#list of items contained in [] surrounded by '' separated by commas
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

#[0] accesses the first item in a list
print(bicycles[0])
print(bicycles[0].title())

#[-1] access the last item in a list
print(bicycles[-1])

print(f"My first bicycle was a {bicycles[0].title()}")